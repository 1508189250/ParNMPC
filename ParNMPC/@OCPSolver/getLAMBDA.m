function LAMBDA = getLAMBDA(solver,x0,lambda,mu,u,x,p)
xDim = solver.OCP.dim.x;
N    = solver.OCP.N;
discretizationMethod = solver.OCP.discretizationMethod;
isMEnabled = solver.OCP.isMEnabled;

LAMBDA = zeros(xDim,xDim,N);
[lambda,...
 mu,...
 u,...
 x,...
 LAMBDA,...
 cost,...
 error,...
 timeElapsed] = NMPC_Iter(x0,...
                          lambda,...
                          mu,...
                          u,...
                          x,...
                          p,...
                          LAMBDA,...
                          discretizationMethod,...
                          isMEnabled);
end